class RgbBase
{
protected:
    double r, g, b;

    RgbBase()
    {
        r = 0;
        g = 0;
        b = 0;
    }

    RgbBase(double xr, double xg, double xb) : r(xr), g(xg), b(xb)
    {
    }

public:
    double get_r() const noexcept { return r; }
    double get_g() const noexcept { return g; }
    double get_b() const noexcept { return b; }
};

class LinearRgb final : public RgbBase
{
    static constexpr double HCLgamma = 3;
    static constexpr double HCLy0 = 100;
    static constexpr double HCLmaxL = 0.5304545339535168556124399538312; // == exp(HCLgamma / HCLy0) - 0.5
    static constexpr double PI = 3.14159265358979323846264338327950288;

    static double frac(double x)
    {
        double integer;

        return modf(x, &integer);
    }

public:
    void SetHCL(const double h, const double c, const double l)
    {
        // http://www.chilliant.com/rgb2hsv.html

        r = 0, g = 0, b = 0;

        if (0 == l)
            return;

        auto H = h;
        auto C = c;
        auto L = l * HCLmaxL;
        auto Q = exp((1 - C / (2 * L)) * (HCLgamma / HCLy0));
        auto U = (2 * L - C) / (2 * Q - 1);
        auto V = C / Q;
        auto T = tan((H + std::min(frac(2 * H) / 4, frac(-2 * H) / 8)) * PI * 2);

        H *= 6;

        if (H <= 1)
        {
            r = 1;
            g = T / (1 + T);
        }
        else if (H <= 2)
        {
            r = (1 + T) / T;
            g = 1;
        }
        else if (H <= 3)
        {
            g = 1;
            b = 1 + T;
        }
        else if (H <= 4)
        {
            g = 1 / (1 + T);
            b = 1;
        }
        else if (H <= 5)
        {
            r = -1 / T;
            b = 1;
        }
        else
        {
            r = 1;
            b = -T;
        }

        r = r * V + U;
        g = g * V + U;
        b = b * V + U;
    }
};

class sRgb final : public RgbBase
{
    static constexpr double theta = 12.92;
    static constexpr double threshold = 0.0031308;
    static constexpr double a = 0.055;
    static constexpr double gamma = 2.4;

    static double c_gamma(const double c_linear)
    {
        if (c_linear <= threshold)
            return theta * c_linear;

        return (1 + a) * pow(c_linear, 1 / gamma);
    }

public:
    sRgb(double xr, double xg, double xb) : RgbBase(xr, xg, xb)
    {
    }

    void set(const LinearRgb& linear_rgb)
    {
        r = c_gamma(linear_rgb.get_r());
        g = c_gamma(linear_rgb.get_g());
        b = c_gamma(linear_rgb.get_b());
    }
};

class ColorMapBase
{
    Eigen::Spline3d spline_;
    int index_;
    const std::array<sRgb, 256>* colormap_;
protected:
    sRgb interpolate(const double v, const std::array<sRgb, 256>& colormap);

    static uint8_t clamped_to_255(const double c)
    {
        const auto int_c = static_cast<int>(c * 255 + 0.5);

        if (int_c <= 0)
            return 0;
        if (int_c >= 255)
            return 255;

        return static_cast<uint8_t>(int_c);
    }

    template<int Size>
    std::map<double, uint32_t> interpolate_colormap(const std::array<sRgb, Size>& colormap, const int multiplier);
public:
    static uint32_t to_rgba(const sRgb& rgb);
};

sRgb ColorMapBase::interpolate(const double v, const std::array<sRgb, 256>& colormap)
{
    const auto target_index = colormap.size() * v;

    if (target_index <= 0)
        return *colormap.begin();

    if (target_index >= colormap.size())
        return *colormap.rbegin();

    auto index = static_cast<int>(target_index - 1 + 0.5);

    if (index < 0)
        index = 0;
    else if (index > colormap.size() - 3)
        index = colormap.size() - 3;

    // See if we can use the cached spline...
    if (colormap_ != &colormap || index != index_)
    {
        Eigen::MatrixXd path(3, 3);

        for (auto i = 0; i < 3; ++i)
        {
            path(0, i) = colormap[index + i].get_r();
            path(1, i) = colormap[index + i].get_g();
            path(2, i) = colormap[index + i].get_b();
        }

        Eigen::VectorXd knots{ 3 };
        knots << 0, 0.5, 1;

        spline_ = Eigen::SplineFitting<Eigen::Spline3d>::Interpolate(path, 2, knots.transpose());
        index_ = index;
        colormap_ = &colormap;
    }

    auto rgb = spline_((target_index - index) * 0.5);

    return sRgb(rgb(0), rgb(1), rgb(2));
}

uint32_t ColorMapBase::to_rgba(const sRgb& rgb)
{
    const auto r255 = clamped_to_255(rgb.get_r());
    const auto g255 = clamped_to_255(rgb.get_g());
    const auto b255 = clamped_to_255(rgb.get_b());

    return (r255 << 24) | (g255 << 16) | (b255 << 8);
}

